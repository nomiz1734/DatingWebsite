    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        try {
            HttpSession session = request.getSession();
            userAccount user = (userAccount) session.getAttribute("user");
            if (user == null) {
                response.sendRedirect("login.jsp");
                return;
            }
            String fullName = request.getParameter("fullname");
            int genderId = Integer.parseInt(request.getParameter("genderId"));
            String details = request.getParameter("details");
            String address = request.getParameter("address");
            String face = request.getParameter("facebook");
            String folderName = "ava";
            String temp = request.getServletContext().getRealPath("");
            String uploadPath = temp + "\\" + folderName;
            File dir = new File(uploadPath);
            if (!dir.exists()) {
                dir.mkdirs();
            }
            Part filePart = request.getPart("avatar");
            String fileName = filePart.getSubmittedFileName();
            String path = folderName + "\\" + fileName;
            File file = new File(uploadPath + "\\" + fileName);
            if (!file.exists()) {
                InputStream is = filePart.getInputStream();
                Files.copy(is, file.toPath(), StandardCopyOption.REPLACE_EXISTING);
            }
            userAccount editedUser = new userAccount(user.getUsername(), fullName, genderId, details, address, path, face);
            userDAO dao = new userDAO();
            dao.updateUserProfile(editedUser);

            session.setAttribute("user", editedUser);
            response.sendRedirect("profile.jsp");
        } catch (Exception ex) {
            Logger.getLogger(EditProfileServlet.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

